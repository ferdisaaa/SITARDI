    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
                                               
    try {
        // 1. Membuat objek Pemilih baru
        object.Pemilih P = new object.Pemilih();
        
        // 2. Set semua data dari textfield ke objek Pemilih
        P.setNik(TxtNIK.getText());
        P.setNama_lengkap(TxtNama.getText());
        P.setDomisili(TxtDomisili.getText());
        P.setStatus_pemilih(TxtStatus.getText());
        P.setJenis_kelamin(cmbJenisKelamin.getSelectedItem().toString());
        
        // 3. Khusus Tanggal Lahir: Ubah String dari textfield ke tipe Date Java
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd-MM-yyyy");
        java.util.Date tanggal = sdf.parse(TxtTTL.getText());
        P.setTanggal_lahir(tanggal);
        
        // 4. Panggil service untuk menyimpan ke database MongoDB
        services.PemilihService service = new services.PemilihService();
        service.tambahPemilih(P);
        
        // 5. Refresh data kartu dan tutup form
        com.sitardi.sitardi.Panels.DataPemilih.showData("");
        this.dispose();
        
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan data! Pastikan format tanggal benar (dd-MM-yyyy)");
    }

    }//GEN-LAST:event_btnSaveActionPerformed

