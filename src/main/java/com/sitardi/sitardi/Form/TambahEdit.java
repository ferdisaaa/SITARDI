    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
        // 1. Membuat objek Pemilih baru untuk menampung data yang di-update
        object.Pemilih P = new object.Pemilih();
        
        // 2. Mengambil input dari textfield form (menggunakan huruf kecil sesuai Pemilih.java baru)
        P.setNik(TxtNIK.getText().trim());
        P.setNama_lengkap(TxtNama.getText().trim());
        P.setDomisili(TxtDomisili.getText().trim());
        P.setStatus_pemilih(TxtStatus.getText().trim());
        P.setJenis_kelamin(cmbJenisKelamin.getSelectedItem().toString());
        
        // 3. Memformat String tanggal dari kolom menjadi objek Date Java
        String txtTanggal = TxtTTL.getText().trim();
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd-MM-yyyy");
        java.util.Date tanggal = sdf.parse(txtTanggal);
        P.setTanggal_lahir(tanggal);
        
        // 4. Memanggil PemilihService untuk mengeksekusi update ke MongoDB
        services.PemilihService service = new services.PemilihService();
        service.updatePemilih(P);
        
        // 5. Menutup form pop-up setelah sukses update
        this.dispose();
        
    } catch (java.text.ParseException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Format tanggal salah! Gunakan format dd-MM-yyyy (Contoh: 02-11-1990)");
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengupdate data: " + e.getMessage());
    }
    
    }//GEN-LAST:event_btnUpdateActionPerformed

