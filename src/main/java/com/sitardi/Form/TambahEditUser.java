    public void loadProfileImage(String urlString) {
        if (urlString == null || urlString.trim().isEmpty()) {
            // Jika URL kosong, bisa dikembalikan ke gambar default
            roundImage1.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/nanami_MBG(My Bini Gwejh).png")));
            return;
        }

        // Jalankan di thread terpisah agar UI tidak freeze
        new Thread(() -> {
            try {
                java.net.URL url = new java.net.URL(urlString);
                java.awt.Image image = javax.imageio.ImageIO.read(url);

                if (image != null) {
                    // Ambil ukuran komponen roundImage1
                    int width = roundImage1.getWidth();
                    int height = roundImage1.getHeight();

                    // Jika width/height masih 0 (karena belum render), beri ukuran default
                    if (width <= 0) {
                        width = 130;
                    }
                    if (height <= 0) {
                        height = 130;
                    }

                    // Scale gambar agar pas dengan komponen
                    java.awt.Image scaledImage = image.getScaledInstance(width, height, java.awt.Image.SCALE_SMOOTH);

                    // Update UI di Event Dispatch Thread
                    javax.swing.SwingUtilities.invokeLater(() -> {
                        roundImage1.setIcon(new javax.swing.ImageIcon(scaledImage));
                    });
                }
            } catch (Exception e) {
                System.err.println("Gagal memuat gambar dari URL: " + e.getMessage());
                // Jika error, biarkan gambar lama atau set placeholder
            }
        }).start();
    }

