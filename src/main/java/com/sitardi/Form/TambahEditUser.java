    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            // 1. Ambil data dari inputan
            User u = new User();
            u.setNik(TxtNIK.getText().trim());
            u.setUsername(TxtUsername.getText().trim());
            u.setName(TxtNama.getText().trim());
            u.setEmail(Txtemail.getText().trim());
            u.setPassword(new String(TxtPassword.getPassword()));
            u.setRole(cmbRole.getSelectedItem().toString());
            u.setUrl_img(txtUrl.getText().trim());

            // 2. Validasi sederhana
            if (u.getNik().isEmpty() || u.getUsername().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "NIK dan Username tidak boleh kosong!");
                return;
            }

            // 3. Eksekusi Service
            UserService service = new UserService();
            service.tambahUser(u);

            // 4. Feedback & Refresh Tabel
            javax.swing.JOptionPane.showMessageDialog(this, "User berhasil disimpan!");
            com.sitardi.Panels.DataUser.showData(""); // Refresh tabel di panel utama
            this.dispose();

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
        }

    }//GEN-LAST:event_btnSaveActionPerformed

